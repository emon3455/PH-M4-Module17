// addition operation
var apple = 10;
var mango = 15;
var totall = apple + mango;
console.log(totall);


// substraction operation:
var difference = mango - apple;
console.log(difference);

// multiplication
var quantity = 7;
var price = apple * quantity;
console.log(price);

// division 
var totallFuitCost = 56;
var totallFruitsAmount = 7;
var eachFruitsPrice = totallFuitCost / totallFruitsAmount;
console.log(eachFruitsPrice);


// remainder operation
var juice = 10;
var people = 3;
var remainder = juice % people;
console.log(remainder);


